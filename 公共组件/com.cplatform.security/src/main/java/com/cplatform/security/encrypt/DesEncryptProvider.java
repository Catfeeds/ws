package com.cplatform.security.encrypt;

public class DesEncryptProvider extends SymmetryEncryptProvider {

	@Override
	public byte[] generateKeyBytes(int keySize) throws Exception {
		return super.generateKeyBytes(56);
	}

	@Override
	public String generateKeyHex(int keySize) throws Exception {
		return super.generateKeyHex(56);
	}

	@Override
	public String getAlgorithm() {
		return "DES";
	}

}
