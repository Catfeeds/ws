CREATE TABLE "LIFE".t_page_module (
  "ID" NUMBER(8) NOT NULL,
  code VARCHAR2(20 BYTE),
  title VARCHAR2(50 BYTE),
  temp_id NUMBER(8),
  region_code VARCHAR2(200 BYTE),
  create_user NUMBER(9),
  create_time VARCHAR2(14 BYTE),
  update_user NUMBER(9),
  update_time VARCHAR2(14 BYTE),
  remark VARCHAR2(300 BYTE),
  CONSTRAINT pk_t_page_module PRIMARY KEY ("ID")
);
COMMENT ON TABLE "LIFE".t_page_module IS '页面模块表';
COMMENT ON COLUMN "LIFE".t_page_module.code IS '模块编码';
COMMENT ON COLUMN "LIFE".t_page_module.title IS '模块标题';
COMMENT ON COLUMN "LIFE".t_page_module.temp_id IS '关联数据模板';
COMMENT ON COLUMN "LIFE".t_page_module.region_code IS '省地市编码';
COMMENT ON COLUMN "LIFE".t_page_module.create_user IS '创建人';
COMMENT ON COLUMN "LIFE".t_page_module.create_time IS '创建时间';
COMMENT ON COLUMN "LIFE".t_page_module.update_user IS '修改人';
COMMENT ON COLUMN "LIFE".t_page_module.update_time IS '修改时间';
COMMENT ON COLUMN "LIFE".t_page_module.remark IS '描述';