package com.cplatform.mall.back.sys.entity;

import javax.persistence.Basic;
import javax.persistence.Entity;

import com.cplatform.mall.back.entity.IdEntity;

@javax.persistence.Table(name = "T_SYS_USER_REGION")
@Entity
public class SysUserRegion extends IdEntity {

	private Long userId;

	@javax.persistence.Column(name = "USER_ID")
	@Basic
	public Long getUserId() {
		return userId;
	}

	public void setUserId(Long userId) {
		this.userId = userId;
	}

	private String regionCode;

	@javax.persistence.Column(name = "REGION_CODE")
	@Basic
	public String getRegionCode() {
		return regionCode;
	}

	public void setRegionCode(String regionCode) {
		this.regionCode = regionCode;
	}

}
