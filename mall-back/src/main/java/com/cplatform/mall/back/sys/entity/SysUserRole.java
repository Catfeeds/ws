package com.cplatform.mall.back.sys.entity;

import javax.persistence.Basic;
import javax.persistence.Entity;

import com.cplatform.mall.back.entity.IdEntity;

@javax.persistence.Table(name = "T_SYS_USER_ROLE")
@Entity
public class SysUserRole extends IdEntity {

	private Long userId;

	@javax.persistence.Column(name = "USER_ID")
	@Basic
    public Long getUserId() {
    	return userId;
    }

	
    public void setUserId(Long userId) {
    	this.userId = userId;
    }

    @javax.persistence.Column(name = "ROLE_ID")
	@Basic
    public Long getRoleId() {
    	return roleId;
    }

	
    public void setRoleId(Long roleId) {
    	this.roleId = roleId;
    }

	private Long roleId;

}
