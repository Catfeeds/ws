@javax.persistence.Table(name = "T_SYS_MENU")
@Entity
public class SysMenu {
    private String menuCode;

    @javax.persistence.Column(name = "MENU_CODE")
    @Id
    public String getMenuCode() {
        return menuCode;
    }

    public void setMenuCode(String menuCode) {
        this.menuCode = menuCode;
    }

    private String menuName;

    @javax.persistence.Column(name = "MENU_NAME")
    @Basic
    public String getMenuName() {
        return menuName;
    }

    public void setMenuName(String menuName) {
        this.menuName = menuName;
    }

    private String menuPcode;

    @javax.persistence.Column(name = "MENU_PCODE")
    @Basic
    public String getMenuPcode() {
        return menuPcode;
    }

    public void setMenuPcode(String menuPcode) {
        this.menuPcode = menuPcode;
    }

    private String menuUrl;

    @javax.persistence.Column(name = "MENU_URL")
    @Basic
    public String getMenuUrl() {
        return menuUrl;
    }

    public void setMenuUrl(String menuUrl) {
        this.menuUrl = menuUrl;
    }

    private boolean leafYn;

    @javax.persistence.Column(name = "LEAF_YN")
    @Basic
    public boolean isLeafYn() {
        return leafYn;
    }

    public void setLeafYn(boolean leafYn) {
        this.leafYn = leafYn;
    }

    private String modelCode;

    @javax.persistence.Column(name = "MODEL_CODE")
    @Basic
    public String getModelCode() {
        return modelCode;
    }

    public void setModelCode(String modelCode) {
        this.modelCode = modelCode;
    }

    private String urlBtns;

    @javax.persistence.Column(name = "URL_BTNS")
    @Basic
    public String getUrlBtns() {
        return urlBtns;
    }

    public void setUrlBtns(String urlBtns) {
        this.urlBtns = urlBtns;
    }

    private int sysType;

    @javax.persistence.Column(name = "SYS_TYPE")
    @Basic
    public int getSysType() {
        return sysType;
    }

    public void setSysType(int sysType) {
        this.sysType = sysType;
    }

}
